/**
 * Приводит market_fit_summary к обращению на «вы» (тет-а-тет).
 * Страховка от LLM/кэша в третьем лице.
 */
export function toSecondPersonMarketFit(text: string): string {
  let s = text.trim();
  if (!s) return s;

  s = s.replace(/Опыт\s+[A-ZА-ЯЁ][\wа-яёА-ЯЁ'-]*\s+(в|по)\s+/giu, 'Ваш опыт $1 ');
  s = s.replace(/опыт\s+[A-ZА-ЯЁ][\wа-яёА-ЯЁ'-]*\s+(в|по)\s+/giu, 'ваш опыт $1 ');

  s = s.replace(/Е[её]\s+достижения/giu, 'Ваши достижения');
  s = s.replace(/Его\s+достижения/giu, 'Ваши достижения');
  s = s.replace(/е[её]\s+достижения/giu, 'ваши достижения');
  s = s.replace(/его\s+достижения/giu, 'ваши достижения');

  s = s.replace(/Е[её]\s+навыки/giu, 'Ваши навыки');
  s = s.replace(/Его\s+навыки/giu, 'Ваши навыки');
  s = s.replace(/е[её]\s+навыки/giu, 'ваши навыки');
  s = s.replace(/его\s+навыки/giu, 'ваши навыки');

  s = s.replace(
    /делает\s+е[её]\s+конкурентоспособной\s+кандидатурой/giu,
    'делает вас конкурентоспособным кандидатом'
  );
  s = s.replace(
    /делает\s+его\s+конкурентоспособным\s+кандидатом/giu,
    'делает вас конкурентоспособным кандидатом'
  );

  s = s.replace(/где\s+она\s+сможет/giu, 'где вы сможете');
  s = s.replace(/где\s+он\s+сможет/giu, 'где вы сможете');
  s = s.replace(/,\s*где\s+она(?=[\s,.!?]|$)/giu, ', где вы');
  s = s.replace(/,\s*где\s+он(?=[\s,.!?]|$)/giu, ', где вы');

  s = s.replace(/(^|[^\p{L}])е[её](?=[^\p{L}]|$)/giu, '$1ваш');
  s = s.replace(/(^|[^\p{L}])его(?=[^\p{L}]|$)/giu, '$1ваш');

  s = s.replace(/Рекомендуем\s+(?!вам\b)/giu, 'Рекомендуем вам ');
  s = s.replace(/\s{2,}/g, ' ').trim();

  return s;
}
