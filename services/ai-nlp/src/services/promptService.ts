type PromptSectionKey = keyof typeof PROMPT_SECTIONS;

function buildSystemPrompt(options?: {
  excludeSections?: PromptSectionKey[];
  extraSections?: string[];
}): string {
  const exclude = new Set(options?.excludeSections ?? []);
  const baseSections = (Object.keys(PROMPT_SECTIONS) as PromptSectionKey[])
    .filter((key) => !exclude.has(key))
    .map((key) => PROMPT_SECTIONS[key]);

  const extraSections = options?.extraSections ?? [];

  return [...baseSections, ...extraSections].join('\n\n');
}

const BASE_SYSTEM_PROMPT = buildSystemPrompt();

export function buildSystemMessage(options?: {
  excludeSections?: PromptSectionKey[];
  extraSections?: string[];
}): AIMessage {
  return {
    role: 'system',
    text: options ? buildSystemPrompt(options) : BASE_SYSTEM_PROMPT,
  };
}

export function buildUserMessage(content: string): AIMessage {
  return {
    role: 'user',
    text: content,
  };
}

export function buildAssistantMessage(content: string): AIMessage {
  return {
    role: 'assistant',
    text: content,
  };
}
